package practice.reflection;

public class OperationService {

  public int add(int a, int b) {
    return a + b;
  }

  public int subtract(int a, int b) {
    return a - b;
  }

  public int multiply(int a, int b) {
    return a * b;
  }

  private int privateDivide(int a, int b) {
    return a / b;
  }
}
